uint8_t GetCRC8(std::vector<uint8_t> ValArray)
{
    uint8_t CRC = 0;
    for (unsigned int i = 0; i < ValArray.size(); i++)
        CRC = (uint8_t)((CRC >> 8) ^ Crc8Tab[(uint8_t)CRC ^ ValArray[i]]);
    return CRC;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
uint16_t GetCRC16(std::vector<uint8_t> ValArray, int start_index)
{
    uint16_t wCRC = 0;

    for (unsigned int i = start_index; i < ValArray.size(); i++)
        wCRC = (uint16_t)((wCRC >> 8) ^ Crc16Tab[(uint8_t)wCRC ^ ValArray[i]]);
    return wCRC;

}

