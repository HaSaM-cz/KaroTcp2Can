std::vector<uint8_t>& PrepareTcpData(std::vector<uint8_t> &data, uint8_t tcp_begin, uint8_t tcp_end)
{
    auto it = data.begin();
    while (it != data.end())
    {
        if (((*it) & 0xF0) == 0xF0)
        {
            uint8_t byte = (*it) & 0x0F;
            (*it) = 0xF0;
            it = data.insert(++it, byte);
        }
        it++;
    }

    data.insert(data.begin(), tcp_begin); // zahlavi
    data.push_back(tcp_end);              // zapati
    
    return data;
}

